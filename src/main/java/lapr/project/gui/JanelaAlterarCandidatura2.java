    private void loadData() {
        jTextField1.setText(this.controller.getDadosCandidatura().getNome_Empresa());
        jTextField2.setText(this.controller.getDadosCandidatura().getMorada_Empresa());
        jTextField3.setText(String.valueOf(this.controller.getDadosCandidatura().getTelemovel_Empresa()));
        jTextField4.setText(String.valueOf(this.controller.getDadosCandidatura().getArea_Empresa()));
        jTextField5.setText(String.valueOf(this.controller.getDadosCandidatura().getQtd_convites()));
        
        dlmProd.clear();
        for(Produto p:this.controller.getListProdutos()){
            dlmProd.addElement(p);          
        }
        jList1.setModel(dlmProd);
    }

    private void carregaProdutoInfo() {
        jTextField6.setText(this.controller.getDadosProdutoSelected().getDesignacao_produto());
        this.dlmPlvChvProd.clear();
        for(String str:this.controller.getDadosProdutoSelected().getPalavras_chave_produto()){
            dlmPlvChvProd.addElement(str);
        }
        jList2.setModel(dlmPlvChvProd);
    }
